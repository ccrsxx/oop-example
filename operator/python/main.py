# declare variables
a, b = 12, 5

# print variables
print(f'{a=}, {b=}')

# addition operator
print(f'a + b = {a + b}')

# subtraction operator
print(f'a - b =  {a - b}')

# multiplication operator
print(f'a * b =  {a * b}')

# division operator
print(f'a / b =  {a / b}')

# modulo operator
print(f'a % b =  {a % b}')
