// declare variables
const [a, b] = [12, 5];

// print variables
console.log(`a = ${a}, b = ${b}`);

// addition operator
console.log(`a + b = ${a + b}`);

// subtraction operator
console.log(`a - b =  ${a - b}`);

// multiplication operator
console.log(`a * b =  ${a * b}`);

// division operator
console.log(`a / b =  ${a / b}`);

// modulo operator
console.log(`a % b =  ${a % b}`);
